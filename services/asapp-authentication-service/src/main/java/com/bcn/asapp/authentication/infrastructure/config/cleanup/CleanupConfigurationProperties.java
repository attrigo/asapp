package com.bcn.asapp.authentication.infrastructure.config.cleanup;

import org.springframework.boot.context.properties.ConfigurationProperties;
import org.springframework.stereotype.Component;

/**
 * Configuration properties for JWT authentication cleanup job.
 *
 * @since 0.3.0
 * @author attrigo
 */
@Component
@ConfigurationProperties(prefix = "asapp.cleanup.jwt")
public class CleanupConfigurationProperties {

    private boolean enabled = true;

    private String cron = "0 0 2 * * ?";

    private int retentionDays = 30;

    private int batchSize = 1000;

    public boolean isEnabled() {
        return enabled;
    }

    public void setEnabled(boolean enabled) {
        this.enabled = enabled;
    }

    public String getCron() {
        return cron;
    }

    public void setCron(String cron) {
        this.cron = cron;
    }

    public int getRetentionDays() {
        return retentionDays;
    }

    public void setRetentionDays(int retentionDays) {
        this.retentionDays = retentionDays;
    }

    public int getBatchSize() {
        return batchSize;
    }

    public void setBatchSize(int batchSize) {
        this.batchSize = batchSize;
    }

}
